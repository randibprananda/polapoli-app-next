import axios from 'axios';
import { NextApiRequest, NextApiResponse } from 'next';
import { API_BACKEND_HOST } from '../../../config';
import cookie from 'cookie';
import formidable from 'formidable';
import { createReadStream } from 'fs';

export const config = {
  api: {
    bodyParser: false
  }
};

async function donasi(req: NextApiRequest, res: NextApiResponse) {
  const cookies = cookie.parse(req.headers.cookie || '');
  const token = cookies.token.replace(/"/g, '');
  if (req.method === 'GET') {
    try {
      const response = await axios.get(
        `${API_BACKEND_HOST}/api/v1/donation/list-all`,
        {
          headers: {
            'Content-Type': 'application/json',
            Authorization: `Bearer ${token}`
          }
        }
      );

      res.status(response.status).json(response.data);
      return;
    } catch (error: any) {
      res.status(error.response.status).json({
        data: error.response.data
      });
      return;
    }
  } else if (req.method === 'POST') {
    try {
      const jsonData: any = await new Promise(function (resolve, reject) {
        const form = new formidable.IncomingForm();
        form.parse(req, async (err, fields, files) => {
          if (err) {
            reject(err);
            return;
          }
          resolve({
            fields,
            files
          });
        });
      });

      var FormData = require('form-data');
      const formData = new FormData();
      formData.append(
        'donation_image',
        createReadStream(jsonData.files.donation_image.filepath)
      );
      formData.append('donation_title', jsonData.fields.donation_title);
      formData.append(
        'donation_description',
        jsonData.fields.donation_description
      );
      formData.append('target_amount', jsonData.fields.target_amount);
      formData.append('is_target', +jsonData.fields.is_target);
      formData.append('batas_akhir', jsonData.fields.batas_akhir);
      formData.append('is_batas', +jsonData.fields.is_batas);

      const response = await axios.post(
        `${API_BACKEND_HOST}/api/v1/donation/add-donation`,
        formData,
        {
          headers: {
            ...formData.getHeaders(),
            Authorization: `Bearer ${token}`
          }
        }
      );

      res.status(response.status).json(response.data);
      return;
    } catch (error: any) {
      console.log(error);
      console.log(error.response.data);
      res.status(error.response.status).json({
        data: error.response.data
      });
      return;
    }
  } else if (req.method === 'PUT') {
    try {
      const jsonData: any = await new Promise(function (resolve, reject) {
        const form = new formidable.IncomingForm();
        form.parse(req, async (err, fields, files) => {
          if (err) {
            reject(err);
            return;
          }
          resolve({
            fields,
            files
          });
        });
      });

      var FormData = require('form-data');
      const formData = new FormData();
      jsonData.files.donation_image &&
        formData.append(
          'donation_image',
          createReadStream(jsonData.files.donation_image.filepath)
        );
      formData.append('donation_title', jsonData.fields.donation_title);
      formData.append(
        'donation_description',
        jsonData.fields.donation_description
      );
      formData.append('target_amount', jsonData.fields.target_amount);
      formData.append('is_target', +jsonData.fields.is_target);
      formData.append('batas_akhir', jsonData.fields.batas_akhir);
      formData.append('is_batas', +jsonData.fields.is_batas);

      const response = await axios.post(
        `${API_BACKEND_HOST}/api/v1/donation/update-donation/${req.query.id}`,
        formData,
        {
          headers: {
            ...formData.getHeaders(),
            Authorization: `Bearer ${token}`
          }
        }
      );

      res.status(response.status).json(response.data);
      return;
    } catch (error: any) {
      console.log(error);
      console.log(error.response.data);
      res.status(error.response.status).json({
        data: error.response.data
      });
      return;
    }
  } else if (req.method === 'DELETE') {
    try {
      const response = await axios.delete(
        `${API_BACKEND_HOST}/api/v1/donation/delete/${req.query.id}`,
        {
          headers: {
            'Content-Type': 'application/json',
            Authorization: `Bearer ${token}`
          }
        }
      );

      res.status(response.status).json(response.data);
      return;
    } catch (error: any) {
      res.status(error.response.status).json({
        data: error.response.data
      });
      return;
    }
  } else {
    res.setHeader('Allow', ['GET']);
    res.status(405).json({
      status: 'error',
      message: `Method ${req.method} not allowed`
    });
  }
}

export default donasi;
